import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
    {
        username: {
            type: String,
            required: true,
            unique: true,
            trim: true,
            lowercase: true,
        },
        hashedPassword: { type: String, required: true },
        email: {
            type: String,
            required: true,
            unique: true,
            trim: true,
            lowercase: true,
        },
        displayName: {
            type: String,
            required: true,
            trim: true,
            lowercase: true,
        },
        avatarUrl: { type: String }, // Link CDN để hiển thị hình
        avatarId: { type: String }, // public_id của avatar trong cloudinary
        bio: { type: String, maxLength: 500 },
        phone: {
            type: String,
            sparse: true, // Cho phép null, không duplicate
        },
    },
    {
        timestamps: true,
    },
);

const User = mongoose.model("User", userSchema);

export default User;
